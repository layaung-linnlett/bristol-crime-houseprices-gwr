    "def plot_gwr_coefficient_map(reg_gdf: gpd.GeoDataFrame, gwr_results: Dict[str, Any], output_path: Path = OUTPUT_PATH):\n",
    "    \"\"\"Map of local GWR crime coefficients.\"\"\"\n",
    "    if gwr_results is None:\n",
    "        print(\"No GWR results available\")\n",
    "        return\n",
    "    \n",
    "    # Add local coefficients to GeoDataFrame\n",
    "    reg_gdf_gwr = reg_gdf.copy()\n",
    "    reg_gdf_gwr['gwr_intercept'] = gwr_results['params'][:, 0]\n",
    "    reg_gdf_gwr['gwr_log_crime'] = gwr_results['params'][:, 1]  # Crime coefficient\n",
    "    reg_gdf_gwr['gwr_prop_flats'] = gwr_results['params'][:, 2]\n",
    "    reg_gdf_gwr['gwr_prop_leasehold'] = gwr_results['params'][:, 3]\n",
    "    \n",
    "    # Plot crime coefficient map\n",
    "    fig, ax = plt.subplots(1, 1, figsize=(10, 10))\n",
    "    \n",
    "    reg_gdf_gwr.plot(column='gwr_log_crime', cmap='bwr', legend=True, \n",
    "                    linewidth=0.2, edgecolor='grey', ax=ax,\n",
    "                    legend_kwds={'label': \"GWR Crime Coefficient\", 'shrink': 0.8})\n",
    "    \n",
    "    ax.set_title('Local Crime Effect on House Prices (GWR) - Bristol', fontsize=14)\n",
    "    ax.axis('off')\n",
    "    \n",
    "    plt.tight_layout()\n",
    "    plt.savefig(output_path / 'map_gwr_crime_coef.png', dpi=300, bbox_inches='tight')\n",
    "    plt.show()\n",
    "    print(\"Saved: map_gwr_crime_coef.png\")\n",
