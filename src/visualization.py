    "def plot_price_distribution(house_clean: pd.DataFrame, output_path: Path = OUTPUT_PATH):\n",
    "    \"\"\"Plot histogram of house prices.\"\"\"\n",
    "    fig, ax = plt.subplots(figsize=(10, 6))\n",
    "    \n",
    "    ax.hist(house_clean['price'], bins=50, color='#ffcbcb', edgecolor='black', alpha=0.7)\n",
    "    ax.set_xlabel('Sale Price (£)', fontsize=12)\n",
    "    ax.set_ylabel('Number of Sales', fontsize=12)\n",
    "    ax.set_title('Distribution of House Prices in Bristol', fontsize=14, pad=12)\n",
    "    \n",
    "    # Format y-axis with thousands separator\n",
    "    ax.get_yaxis().set_major_formatter(plt.FuncFormatter(lambda x, p: f'{x:,.0f}'))\n",
    "    \n",
    "    plt.tight_layout()\n",
    "    plt.savefig(output_path / 'price_distribution.png', dpi=300, bbox_inches='tight')\n",
    "    plt.show()\n",
    "    print(\"Saved: price_distribution.png\")\n",
