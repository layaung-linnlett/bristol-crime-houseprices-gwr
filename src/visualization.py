    "def plot_crime_trends(crime_clean: pd.DataFrame, output_path: Path = OUTPUT_PATH):\n",
    "    \"\"\"Plot crime trends by type over years.\"\"\"\n",
    "    # Aggregate by year and crime type\n",
    "    crime_pivot = crime_clean.pivot_table(\n",
    "        index='year', columns='Crime type', aggfunc='size', fill_value=0\n",
    "    )\n",
    "    \n",
    "    # Top 5 crime types\n",
    "    top_types = crime_clean['Crime type'].value_counts().head(5).index\n",
    "    \n",
    "    fig, ax = plt.subplots(figsize=(12, 6))\n",
    "    for crime_type in top_types:\n",
    "        if crime_type in crime_pivot.columns:\n",
    "            ax.plot(crime_pivot.index, crime_pivot[crime_type], \n",
    "                   marker='o', linewidth=2, label=crime_type)\n",
    "    \n",
    "    ax.set_xlabel('Year', fontsize=12)\n",
    "    ax.set_ylabel('Number of Crimes', fontsize=12)\n",
    "    ax.set_title('Crime Trends by Type in Bristol', fontsize=14, pad=12)\n",
    "    ax.legend(fontsize=9, frameon=True)\n",
    "    ax.grid(alpha=0.3)\n",
    "    \n",
    "    plt.tight_layout()\n",
    "    plt.savefig(output_path / 'crime_trends.png', dpi=300, bbox_inches='tight')\n",
    "    plt.show()\n",
    "    print(\"Saved: crime_trends.png\")\n",
    "\n",
