    "def plot_model_comparison(comparison_results: Dict[str, Any], output_path: Path = OUTPUT_PATH):\n",
    "    \"\"\"Bar chart comparing OLS vs GWR metrics.\"\"\"\n",
    "    if comparison_results.get('comparison_table') is None:\n",
    "        print(\"No comparison results available\")\n",
    "        return\n",
    "    \n",
    "    comparison_df = comparison_results['comparison_table']\n",
    "    \n",
    "    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(12, 5))\n",
    "    \n",
    "    # R² comparison\n",
    "    metrics_to_plot = comparison_df[comparison_df['Metric'].isin(['R²', 'Adj R²'])]\n",
    "    x = np.arange(len(metrics_to_plot))\n",
    "    width = 0.35\n",
    "    \n",
    "    ax1.bar(x - width/2, metrics_to_plot['OLS'], width, label='OLS', alpha=0.8)\n",
    "    ax1.bar(x + width/2, metrics_to_plot['GWR'], width, label='GWR', alpha=0.8)\n",
    "    ax1.set_xlabel('Metrics')\n",
    "    ax1.set_ylabel('R²')\n",
    "    ax1.set_title('Model Fit Comparison (R²)')\n",
    "    ax1.set_xticks(x)\n",
    "    ax1.set_xticklabels(metrics_to_plot['Metric'])\n",
    "    ax1.legend()\n",
    "    \n",
    "    # AICc comparison\n",
    "    ax2.bar(['OLS', 'GWR'], comparison_df[comparison_df['Metric'] == 'AICc'][['OLS', 'GWR']].values[0], \n",
    "            color=['red', 'blue'], alpha=0.8)\n",
    "    ax2.set_ylabel('AICc (lower = better)')\n",
    "    ax2.set_title('Model Fit Comparison (AICc)')\n",
    "    \n",
    "    plt.tight_layout()\n",
    "    plt.savefig(output_path / 'model_comparison.png', dpi=300, bbox_inches='tight')\n",
    "    plt.show()\n",
    "    print(\"Saved: model_comparison.png\")\n",
    "\n",
