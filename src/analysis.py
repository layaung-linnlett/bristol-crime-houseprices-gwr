    "def fit_gwr_model(reg_gdf: pd.GeoDataFrame, config: dict) -> Optional[Dict[str, Any]]:\n",
    "    \"\"\"Fit Geographically Weighted Regression (GWR) model.\"\"\"\n",
    "    if not GWR_AVAILABLE:\n",
    "        print(\"GWR not available (mgwr not installed)\")\n",
    "        return None\n",
    "    \n",
    "    print(\"\\n3. GEOGRAPHICALLY WEIGHTED REGRESSION (GWR)\")\n",
    "    \n",
    "    # Extract coordinates\n",
    "    coords = np.column_stack([\n",
    "        reg_gdf.geometry.centroid.x,\n",
    "        reg_gdf.geometry.centroid.y\n",
    "    ])\n",
    "    \n",
    "    # Outcome and predictors (same as OLS)\n",
    "    y = reg_gdf['log_median_price'].values.reshape(-1, 1)\n",
    "    X = reg_gdf[['log_crime', 'prop_flats', 'prop_leasehold']].values\n",
    "    \n",
    "    print(f\"Input shape: coords={coords.shape}, y={y.shape}, X={X.shape}\")\n",
    "    \n",
    "    if len(reg_gdf) < 30:\n",
    "        print(\"Too few LSOAs for stable GWR fit\")\n",
    "        return None\n",
    "    \n",
    "    # Bandwidth selection\n",
    "    print(\"Bandwidth selection...\")\n",
    "    try:\n",
    "        bw = Sel_BW(coords, y, X).search()\n",
    "        print(f\"Bandwidth: {bw:.1f}\")\n",
    "    except Exception:\n",
    "        print(\"Bandwidth selection failed - using fixed fallback\")\n",
    "        bw = 50  # adaptive fallback\n",
    "    \n",
    "    # Fit GWR\n",
    "    print(\"Fitting GWR...\")\n",
    "    try:\n",
    "        gwr_model = GWR(coords, y, X, bw=bw).fit()\n",
    "        print(gwr_model.summary())\n",
    "        \n",
    "        return {\n",
    "            'model': gwr_model,\n",
    "            'bw': bw,\n",
    "            'r2': gwr_model.R2,\n",
    "            'adj_r2': gwr_model.adjR2,\n",
    "            'aic': gwr_model.aic,\n",
    "            'aicc': gwr_model.aicc,\n",
    "            'params': gwr_model.params,  # Local coefficients for each LSOA\n",
    "        }\n",
    "    except Exception as e:\n",
    "        print(f\"GWR failed: {e}\")\n",
    "        return None\n",
    "\n",
    "def calculate_morans_i(reg_gdf: pd.GeoDataFrame, residuals: np.ndarray) -> Optional[Dict[str, Any]]:\n",
    "    \"\"\"Calculate Moran's I to test spatial autocorrelation in residuals.\"\"\"\n",
    "    if not SPATIAL_STATS_AVAILABLE:\n",
    "        print(\"PySAL not available - skipping Moran's I\")\n",
    "        return None\n",
    "    \n",
    "    print(\"\\nCalculating Moran's I (spatial autocorrelation)...\")\n",
    "    \n",
    "    try:\n",
    "        w = Queen.from_dataframe(reg_gdf)\n",
    "        w.transform = 'r'\n",
    "        moran = Moran(residuals, w)\n",
    "        \n",
    "        print(f\"Moran's I: {moran.I:.4f}\")\n",
    "        print(f\"Expected I: {moran.EI:.4f}\")\n",
    "        print(f\"p-value: {moran.p_sim:.4f}\")\n",
    "        \n",
    "        return {\n",
    "            'I': moran.I,\n",
    "            'EI': moran.EI,\n",
    "            'p_value': moran.p_sim,\n",
    "            'significant': moran.p_sim < 0.05,\n",
    "        }\n",
    "    except Exception as e:\n",
    "        print(f\"Moran's I failed: {e}\")\n",
    "        return None\n",
    "\n",
    "def compare_models(\n",
    "    ols_results: Dict[str, Any], \n",
    "    gwr_results: Optional[Dict[str, Any]], \n",
    "    reg_gdf: pd.GeoDataFrame\n",
    ") -> Dict[str, Any]:\n",
    "    \"\"\"Compare OLS vs GWR models.\"\"\"\n",
    "    print(\"\\nSTEP 8: MODEL COMPARISON (OLS vs GWR)\")\n",
    "    \n",
    "    # OLS results\n",
    "    print(\"1. OLS Results\")\n",
    "    print(f\"   R²:     {ols_results['r2']:.4f}\")\n",
    "    print(f\"   Adj R²: {ols_results['adj_r2']:.4f}\")\n",
    "    print(f\"   AICc:   {ols_results['aicc']:.2f}\")\n",
    "    print(f\"   Crime effect: {ols_results['coefficients']['log_crime']:.4f}\")\n",
    "    \n",
    "    if gwr_results is None:\n",
    "        print(\"2. GWR Results: unavailable (GWR failed)\")\n",
    "        return {'comparison_table': None, 'gwr_available': False}\n",
    "    \n",
    "    # GWR results\n",
    "    print(\"2. GWR Results\")\n",
    "    print(f\"   R²:     {gwr_results['r2']:.4f}\")\n",
    "    print(f\"   Adj R²: {gwr_results['adj_r2']:.4f}\")\n",
    "    print(f\"   AICc:   {gwr_results['aicc']:.2f}\")\n",
    "    crime_coefs = gwr_results['params'][:, 1]  # log_crime column\n",
    "    print(f\"   Crime coef range: {crime_coefs.min():.4f} to {crime_coefs.max():.4f}\")\n",
    "    \n",
    "    # Build comparison table\n",
    "    comparison = pd.DataFrame({\n",
    "        'Metric': ['R²', 'Adj R²', 'AICc'],\n",
    "        'OLS': [ols_results['r2'], ols_results['adj_r2'], ols_results['aicc']],\n",
    "        'GWR': [gwr_results['r2'], gwr_results['adj_r2'], gwr_results['aicc']],\n",
    "    })\n",
    "    \n",
    "    improvement = comparison['GWR'] - comparison['OLS']\n",
    "    comparison['GWR Better'] = ['Yes' if imp < 0 or imp > 0 else 'No' \n",
    "                               for imp in improvement]  # AIC lower=better, R2 higher=better\n",
    "    \n",
    "    print(\"\\nComparison Table:\")\n",
    "    print(comparison.round(4).to_string(index=False))\n",
    "    \n",
    "    return {\n",
    "        'comparison_table': comparison,\n",
    "        'gwr_available': True,\n",
    "    }\n",
    "\n",
    "def run_full_analysis_pipeline(reg_gdf: pd.GeoDataFrame, config: dict) -> Dict[str, Any]:\n",
    "    \"\"\"Run complete analysis pipeline (formerly runfullanalysis).\"\"\"\n",
    "    results = {}\n",
    "    \n",
    "    # Statistical tests\n",
    "    results['test_results'] = perform_statistical_tests(reg_gdf, config)\n",
    "    \n",
    "    # OLS model\n",
    "    results['ols_results'] = fit_ols_model(reg_gdf)\n",
    "    \n",
    "    # Moran's I on OLS residuals\n",
    "    results['morans_results'] = calculate_morans_i(reg_gdf, results['ols_results']['residuals'])\n",
    "    \n",
    "    # GWR model\n",
    "    results['gwr_results'] = fit_gwr_model(reg_gdf, config)\n",
    "    \n",
    "    # Model comparison\n",
    "    results['comparison_results'] = compare_models(\n",
    "        results['ols_results'], results['gwr_results'], reg_gdf\n",
    "    )\n",
    "    \n",
    "    return results\n"
