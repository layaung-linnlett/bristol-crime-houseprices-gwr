    "def test_correlation(df: pd.DataFrame, var1: str, var2: str, alpha: float = 0.05) -> Dict[str, Any]:\n",
    "    \"\"\"Perform Pearson and Spearman correlation tests.\"\"\"\n",
    "    # Pearson (linear)\n",
    "    r_pearson, p_pearson = pearsonr(df[var1], df[var2])\n",
    "    # Spearman (rank-based)\n",
    "    r_spearman, p_spearman = spearmanr(df[var1], df[var2])\n",
    "    \n",
    "    results = {\n",
    "        'pearson_r': r_pearson,\n",
    "        'pearson_p': p_pearson,\n",
    "        'pearson_sig': p_pearson < alpha,\n",
    "        'spearman_r': r_spearman,\n",
    "        'spearman_p': p_spearman,\n",
    "        'spearman_sig': p_spearman < alpha,\n",
    "    }\n",
    "    return results\n",
    "\n",
    "def print_correlation_results(results: Dict[str, Any], var1: str, var2: str):\n",
    "    \"\"\"Print formatted correlation results.\"\"\"\n",
    "    print(f\"\\n1. Correlation Analysis: {var1} vs {var2}\")\n",
    "    print(f\"  Pearson: r={results['pearson_r']:.4f}, p={results['pearson_p']:.4f} \"\n",
    "          f\"({'Significant' if results['pearson_sig'] else 'Not significant'})\")\n",
    "    print(f\"  Spearman: r={results['spearman_r']:.4f}, p={results['spearman_p']:.4f} \"\n",
    "          f\"({'Significant' if results['spearman_sig'] else 'Not significant'})\")\n",
    "\n",
    "def perform_statistical_tests(reg_gdf: pd.DataFrame, config: dict) -> Dict[str, Any]:\n",
    "    \"\"\"Comprehensive statistical testing.\"\"\"\n",
    "    alpha = config['statistical']['significance_level']\n",
    "    \n",
    "    # Test 1: Correlation between log(crime) and log(median_price)\n",
    "    corr_results = test_correlation(reg_gdf, 'log_crime', 'log_median_price', alpha)\n",
    "    print_correlation_results(corr_results, 'log(Crime)', 'log(Price)')\n",
    "    \n",
    "    # Test 2: High-crime vs low-crime areas (t-test)\n",
    "    median_crime = reg_gdf['total_crimes'].median()\n",
    "    high_crime = reg_gdf[reg_gdf['total_crimes'] >= median_crime]['log_median_price']\n",
    "    low_crime = reg_gdf[reg_gdf['total_crimes'] < median_crime]['log_median_price']\n",
    "    \n",
    "    t_stat, p_value = ttest_ind(high_crime, low_crime)\n",
    "    \n",
    "    print(\"\\n2. Hypothesis Test: High-Crime vs Low-Crime Areas\")\n",
    "    print(\"H0: Mean log(price) is equal in high/low crime areas\")\n",
    "    print(\"H1: Mean log(price) differs between high/low crime areas\")\n",
    "    print(f\"  t-statistic: {t_stat:.4f}, p-value: {p_value:.4f}\")\n",
    "    \n",
    "    if p_value < alpha:\n",
    "        print(\"Result: REJECT H0 (p < α)\")\n",
    "        mean_diff = high_crime.mean() - low_crime.mean()\n",
    "        pct_diff = (np.exp(mean_diff) - 1) * 100\n",
    "        print(f\"High-crime areas are {pct_diff:.1f}% {'higher' if pct_diff > 0 else 'lower'} priced\")\n",
    "    else:\n",
    "        print(\"Result: FAIL TO REJECT H0 (p ≥ α)\")\n",
    "        print(\"Interpretation: No statistically significant difference\")\n",
    "    \n",
    "    test_results = {\n",
    "        'correlation': corr_results,\n",
    "        'ttest': {\n",
    "            't_statistic': t_stat,\n",
    "            'p_value': p_value,\n",
    "            'significant': p_value < alpha,\n",
    "            'high_crime_mean': high_crime.mean(),\n",
    "            'low_crime_mean': low_crime.mean(),\n",
    "        }\n",
    "    }\n",
    "    return test_results\n",
