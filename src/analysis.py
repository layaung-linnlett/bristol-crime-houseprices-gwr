    "def fit_ols_model(reg_gdf: pd.DataFrame) -> Dict[str, Any]:\n",
    "    \"\"\"Fit global OLS regression model (baseline).\"\"\"\n",
    "    print(\"\\n1. ORDINARY LEAST SQUARES (OLS) REGRESSION - GLOBAL MODEL\")\n",
    "    \n",
    "    # Prepare data: log_price ~ log_crime + prop_flats + prop_leasehold\n",
    "    y = reg_gdf['log_median_price'].values.reshape(-1, 1)\n",
    "    X = reg_gdf[['log_crime', 'prop_flats', 'prop_leasehold']].values\n",
    "    \n",
    "    # Fit OLS\n",
    "    ols = LinearRegression()\n",
    "    ols.fit(X, y)\n",
    "    \n",
    "    # Predictions and residuals\n",
    "    y_pred = ols.predict(X)\n",
    "    residuals = y.ravel() - y_pred.ravel()\n",
    "    \n",
    "    # Metrics\n",
    "    r2 = r2_score(y, y_pred)\n",
    "    n, k = len(y), X.shape[1] + 1  # +1 for intercept\n",
    "    adj_r2 = 1 - (1 - r2) * (n - 1) / (n - k)\n",
    "    mse = mean_squared_error(y, y_pred)\n",
    "    rss = np.sum(residuals**2)\n",
    "    aic = n * np.log(rss/n) + 2 * k\n",
    "    aicc = aic + 2 * k * (k + 1) / (n - k - 1)\n",
    "    \n",
    "    print(\"log_price = β0 + β1*log_crime + β2*prop_flats + β3*prop_leasehold\")\n",
    "    print()\n",
    "    print(f\"  Intercept:     {ols.intercept_[0]:.4f}\")\n",
    "    print(f\"  log_crime:     {ols.coef_[0][0]:.4f}\")\n",
    "    print(f\"  prop_flats:    {ols.coef_[0][1]:.4f}\")\n",
    "    print(f\"  prop_leasehold:{ols.coef_[0][2]:.4f}\")\n",
    "    print(f\"\\nFit Statistics:\")\n",
    "    print(f\"  R²:            {r2:.4f}\")\n",
    "    print(f\"  Adj R²:        {adj_r2:.4f}\")\n",
    "    print(f\"  AIC:           {aic:.2f}\")\n",
    "    print(f\"  AICc:          {aicc:.2f}\")\n",
    "    print(f\"  RMSE:          {np.sqrt(mse):.4f}\")\n",
    "    \n",
    "    # Crime effect interpretation (% change)\n",
    "    crime_effect_pct = (np.exp(ols.coef_[0][0]) - 1) * 100\n",
    "    print(f\"A 1-unit increase in log(crime) → {crime_effect_pct:.2f}% change in price\")\n",
    "    \n",
    "    return {\n",
    "        'model': ols,\n",
    "        'y_pred': y_pred.ravel(),\n",
    "        'residuals': residuals,\n",
    "        'r2': r2,\n",
    "        'adj_r2': adj_r2,\n",
    "        'aic': aic,\n",
    "        'aicc': aicc,\n",
    "        'rmse': float(np.sqrt(mse)),\n",
    "        'coefficients': {\n",
    "            'intercept': float(ols.intercept_[0]),\n",
    "            'log_crime': float(ols.coef_[0][0]),\n",
    "            'prop_flats': float(ols.coef_[0][1]),\n",
    "            'prop_leasehold': float(ols.coef_[0][2]),\n",
    "        }\n",
    "    }\n",
