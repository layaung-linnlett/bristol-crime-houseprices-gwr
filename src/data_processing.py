    "def create_regression_dataset(\n",
    "    lsoa_house: pd.DataFrame, \n",
    "    lsoa_crime: pd.DataFrame, \n",
    "    lsoa_gdf: gpd.GeoDataFrame\n",
    ") -> gpd.GeoDataFrame:\n",
    "    \"\"\"Merge house price, crime, and spatial data into final regression dataset.\"\"\"\n",
    "    \n",
    "    # Merge house prices and crime counts\n",
    "    reg_df = lsoa_house.merge(lsoa_crime, on='lsoa_code', how='inner')\n",
    "    print(f\"After merging house+crime: {len(reg_df)} LSOAs\")\n",
    "    \n",
    "    # Ensure LSOA code column name in spatial data is standardised\n",
    "    if 'lsoacode' not in lsoa_gdf.columns:\n",
    "        for col in ['LSOA21CD', 'lsoa21cd', 'LSOA Code']:\n",
    "            if col in lsoa_gdf.columns:\n",
    "                lsoa_gdf = lsoa_gdf.rename(columns={col: 'lsoa_code'})\n",
    "                break\n",
    "    \n",
    "    # Merge with spatial geometries\n",
    "    reg_gdf = lsoa_gdf.merge(reg_df, on='lsoa_code', how='inner')\n",
