    "def clean_house_prices(house_data: pd.DataFrame, config: dict) -> pd.DataFrame:\n",
    "    \"\"\"Clean and filter house price data for Bristol.\"\"\"\n",
    "    print(\"1. Cleaning house price data...\")\n",
    "    df = house_data.copy()\n",
    "    initial_count = len(df)\n",
    "    \n",
    "    # 1. Filter for Bristol district\n",
    "    df = df[df['district'].str.upper().str.contains(config['geographic']['bristol_district'], na=False)].copy()\n",
    "    print(f\"Filtered to {config['geographic']['bristol_district']}: {len(df)} records\")\n",
    "    \n",
    "    # 2. Standardise price and postcode\n",
    "    df['price'] = pd.to_numeric(df['price'], errors='coerce')\n",
    "    df['postcode'] = df['postcode'].astype(str).str.strip().str.upper()\n",
    "    \n",
    "    # 3. Remove rows with missing critical fields\n",
    "    required_fields = ['price', 'date', 'postcode', 'property_type']\n",
    "    before_missing = len(df)\n",
    "    df = df.dropna(subset=required_fields)\n",
    "    print(f\"Removed {before_missing - len(df)} rows with missing data\")\n",
    "    \n",
    "    # 4. Remove clearly invalid prices\n",
    "    df = df[df['price'] >= 100]\n",
    "    \n",
    "    # 5. Remove outliers using IQR rule\n",
    "    Q1 = df['price'].quantile(0.25)\n",
    "    Q3 = df['price'].quantile(0.75)\n",
    "    IQR = Q3 - Q1\n",
    "    lower_bound = Q1 - config['statistical']['iqr_multiplier'] * IQR\n",
    "    upper_bound = Q3 + config['statistical']['iqr_multiplier'] * IQR\n",
    "    \n",
    "    before_outlier = len(df)\n",
    "    df = df[(df['price'] >= lower_bound) & (df['price'] <= upper_bound)]\n",
    "    print(f\"Removed {before_outlier - len(df)} outliers (IQR method)\")\n",
    "    print(f\"Price range: £{df['price'].min():,.0f} - £{df['price'].max():,.0f}\")\n",
    "    print(f\"Median price: £{df['price'].median():,.0f}\")\n",
    "    \n",
    "    retention_rate = len(df) / initial_count * 100\n",
    "    print(f\"Final: {len(df)} records ({retention_rate:.1f}% retained)\")\n",
    "    return df\n",
