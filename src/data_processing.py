    "def aggregate_house_prices_by_lsoa(house_clean: pd.DataFrame, postcode_lookup: pd.DataFrame) -> pd.DataFrame:\n",
    "    \"\"\"Aggregate cleaned house price data to LSOA level.\"\"\"\n",
    "    print(\"1. Aggregate House Price by LSOA\")\n",
    "    \n",
    "    # Standardise postcode format\n",
    "    house_clean['postcode_clean'] = house_clean['postcode'].astype(str).str.strip().str.upper()\n",
    "    postcode_lookup['postcode_clean'] = postcode_lookup['postcode'].astype(str).str.strip().str.upper()\n",
    "    \n",
    "    # Merge with LSOA codes\n",
    "    house_lsoa = house_clean.merge(\n",
    "        postcode_lookup[['postcode_clean', 'lsoacode']],\n",
    "        on='postcode_clean', how='left'\n",
    "    )\n",
    "    \n",
    "    # Check merge success\n",
    "    merge_rate = house_lsoa['lsoacode'].notna().sum() / len(house_lsoa) * 100\n",
    "    print(f\"Matched {merge_rate:.1f}% of house records to LSOA codes\")\n",
    "    \n",
    "    # Drop unmatched records\n",
    "    house_lsoa = house_lsoa.dropna(subset=['lsoacode'])\n",
    "    \n",
    "    # Create indicator variables\n",
    "    house_lsoa['is_flat'] = (house_lsoa['property_type'] == 'F').astype(int)\n",
    "    house_lsoa['is_leasehold'] = (house_lsoa['tenure_type'] == 'L').astype(int)\n",
    "    \n",
    "    # Aggregate to LSOA level\n",
    "    lsoa_agg = house_lsoa.groupby('lsoacode').agg(\n",
    "        median_price=('price', 'median'),\n",
    "        mean_price=('price', 'mean'),\n",
    "        n_sales=('price', 'size'),\n",
    "        flat_count=('is_flat', 'sum'),\n",
    "        leasehold_count=('is_leasehold', 'sum')\n",
    "    ).reset_index()\n",
    "    \n",
    "    # Calculate proportions\n",
    "    lsoa_agg['prop_flats'] = lsoa_agg['flat_count'] / lsoa_agg['n_sales']\n",
    "    lsoa_agg['prop_leasehold'] = lsoa_agg['leasehold_count'] / lsoa_agg['n_sales']\n",
    "    \n",
    "    print(f\"Aggregated to {len(lsoa_agg)} LSOAs\")\n",
    "    print(f\"Mean sales per LSOA: {lsoa_agg['n_sales'].mean():.1f}\")\n",
    "    return lsoa_agg\n",
    "\n",
